import pandas as pd
import json
from jsonschema import validate, ValidationError
import great_expectations as ge

# Load CSV
df = pd.read_csv("raw_data.csv")

# Standardize columns
df["Transaction Date"] = pd.to_datetime(df["Transaction Date"], errors="coerce")
df["Price Per Unit"] = pd.to_numeric(df["Price Per Unit"], errors="coerce")
df["Quantity"] = pd.to_numeric(df["Quantity"], errors="coerce")
df["Total Spent"] = pd.to_numeric(df["Total Spent"], errors="coerce")

# Load schema
with open("schema.json") as f:
    schema = json.load(f)

# JSON Schema validation
invalid_rows = []
for index, row in df.iterrows():
    try:
        validate(instance=row.to_dict(), schema=schema)
    except ValidationError:
        invalid_rows.append(index)

# Great Expectations checks
ge_df = ge.from_pandas(df)

ge_df.expect_column_values_to_not_be_null("Transaction ID")
ge_df.expect_column_values_to_not_be_null("Customer ID")
ge_df.expect_column_values_to_not_be_null("Transaction Date")
ge_df.expect_column_values_to_be_between("Price Per Unit", min_value=0)
ge_df.expect_column_values_to_be_between("Quantity", min_value=0)
ge_df.expect_column_values_to_be_between("Total Spent", min_value=0)

results = ge_df.validate()

# Save invalid records
df.loc[invalid_rows].to_csv("invalid_records.csv", index=False)

